package Practise;

public class Greeting {
	//no para // no retu value
	
	void m1()
	{
		System.out.println("Hello...");
	}
	
	//no para // retu value
	
	String m2()
	{
		return("Hello how are you??");
	}
	
	//takes para // no retu value
	void m3(String name)
	{
		System.out.println("Hello" +  name);
	}
	
	
	//takes para /// returns the value
	
	String m4(String name)
	{
		return("Hello " + name);
	}

}
